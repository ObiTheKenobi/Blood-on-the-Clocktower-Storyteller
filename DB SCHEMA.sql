BEGIN TRANSACTION;
DROP TABLE IF EXISTS "assignments";
CREATE TABLE "assignments" (
	"assignment_id"	INTEGER NOT NULL UNIQUE,
	"game_id"	INTEGER,
	"player_id"	INTEGER,
	"character_id"	INTEGER,
	"team"	TEXT,
	"won"	INTEGER,
	"assigned_by"	TEXT,
	PRIMARY KEY("assignment_id" AUTOINCREMENT),
	CONSTRAINT "character_id" FOREIGN KEY("character_id") REFERENCES "characters"("character_id"),
	CONSTRAINT "game_id" FOREIGN KEY("game_id") REFERENCES "games"("game_id"),
	CONSTRAINT "player_id" FOREIGN KEY("player_id") REFERENCES "players"("player_id")
);
DROP TABLE IF EXISTS "characters";
CREATE TABLE "characters" (
	"character_id"	INTEGER NOT NULL UNIQUE,
	"name"	TEXT NOT NULL,
	"alignment"	TEXT NOT NULL,
	"role_type"	TEXT NOT NULL,
	"base_strength"	REAL,
	PRIMARY KEY("character_id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "games";
CREATE TABLE "games" (
	"game_id"	INTEGER NOT NULL UNIQUE,
	"script_id"	INTEGER,
	"winning_team"	TEXT,
	"player_count"	INTEGER,
	"players_alive"	INTEGER,
	PRIMARY KEY("game_id" AUTOINCREMENT),
	CONSTRAINT "script_id" FOREIGN KEY("script_id") REFERENCES "scripts"("script_id")
);
DROP TABLE IF EXISTS "players";
CREATE TABLE "players" (
	"player_id"	INTEGER NOT NULL UNIQUE,
	"name"	TEXT NOT NULL,
	"elo_good"	REAL NOT NULL DEFAULT 1500,
	"elo_evil"	REAL NOT NULL DEFAULT 1500,
	PRIMARY KEY("player_id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "script_characters";
CREATE TABLE "script_characters" (
	"script_id"	INTEGER,
	"character_id"	INTEGER,
	CONSTRAINT "character_id" FOREIGN KEY("character_id") REFERENCES "characters"("character_id"),
	CONSTRAINT "script_id" FOREIGN KEY("script_id") REFERENCES "scripts"("script_id")
);
DROP TABLE IF EXISTS "scripts";
CREATE TABLE "scripts" (
	"script_id"	INTEGER NOT NULL UNIQUE,
	"name"	TEXT NOT NULL,
	"type"	TEXT,
	PRIMARY KEY("script_id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "type_distribution";
CREATE TABLE "type_distribution" (
	"num_players"	INTEGER NOT NULL,
	"townsfolk"	INTEGER NOT NULL,
	"outsiders"	INTEGER NOT NULL,
	"minions"	INTEGER NOT NULL,
	"demons"	INTEGER NOT NULL,
	PRIMARY KEY("num_players")
);
